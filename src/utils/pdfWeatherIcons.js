// Weather icon mapping function for PDF
export const getPDFWeatherIcon = (
  shortForecast,
  isDaytime = true,
  size = 24
) => {
  if (!shortForecast) return <SunnyIcon width={size} height={size} />;

  const forecast = shortForecast.toLowerCase();

  // Clear/Sunny conditions
  if (forecast.includes("sunny") || forecast.includes("clear")) {
    return isDaytime ? (
      <SunnyIcon width={size} height={size} />
    ) : (
      <ClearNightIcon width={size} height={size} />
    );
  }

  // Cloudy conditions
  if (forecast.includes("cloudy")) {
    return <MostlyCloudyIcon width={size} height={size} />;
  }

  // Rain conditions
  if (forecast.includes("rain") || forecast.includes("shower")) {
    return <RainIcon width={size} height={size} />;
  }

  // Thunderstorm conditions
  if (forecast.includes("thunderstorm")) {
    return <ThunderstormIcon width={size} height={size} />;
  }

  // Fog/Mist conditions
  if (
    forecast.includes("fog") ||
    forecast.includes("mist") ||
    forecast.includes("haze")
  ) {
    return <FogIcon width={size} height={size} />;
  }

  // Default to sunny
  return isDaytime ? (
    <SunnyIcon width={size} height={size} />
  ) : (
    <ClearNightIcon width={size} height={size} />
  );
};
